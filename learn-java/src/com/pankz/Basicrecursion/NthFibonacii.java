package com.pankz.Basicrecursion;
//Find Nth fibonacii number
public class NthFibonacii {
    public static void main(String[] args) {
       int ans= fibonacii(4);
        System.out.println(ans);


    }
    static int fibonacii(int n)
    {
        //base condition
        if(n<2) {
            return n;
        }


        return fibonacii(n-1)+fibonacii(n-2);  //Recc. ralation  -->?not a tail recursion it will wait for others







































































































































































    }
}
